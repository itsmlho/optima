						${unitFormsHtml}
						
						<div class="mb-3">
							<label class="form-label">Konfirmasi Aksesoris Tersedia</label>
							<div class="border p-3 rounded bg-light">
								${aksesorisCheckboxes}
							</div>
							<div class="form-text">Centang aksesoris yang sudah tersedia sesuai permintaan Marketing.</div>
